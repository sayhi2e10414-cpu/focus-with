function detectFocusLocale() {
  const saved = localStorage.getItem(FOCUS_LOCALE_KEY);
  if (saved === "en" || saved === "zh-CN") return saved;
  const languages = navigator.languages?.length ? navigator.languages : [navigator.language || "en"];
  return languages.some(language => String(language).toLowerCase().startsWith("zh")) ? "zh-CN" : "en";
}

let focusLocale = detectFocusLocale();

function t(message, variables = {}) {
  const template = focusLocale === "zh-CN" ? (FOCUS_TRANSLATIONS_ZH_CN[message] || message) : message;
  return template.replace(/\{\{(\w+)\}\}/g, (_, key) => String(variables[key] ?? ""));
}

function focusValueLabel(value) {
  return t(String(value || ""));
}

function focusLocaleName(locale = focusLocale) {
  return locale === "zh-CN" ? t("Simplified Chinese") : t("English");
}

function setFocusLocale(locale) {
  focusLocale = locale === "zh-CN" ? "zh-CN" : "en";
  localStorage.setItem(FOCUS_LOCALE_KEY, focusLocale);
  document.documentElement.lang = focusLocale;
  applyStaticTranslations();
  document.dispatchEvent(new CustomEvent("focus:locale-change", {detail: {locale: focusLocale}}));
}

function toggleFocusLocale() {
  setFocusLocale(focusLocale === "zh-CN" ? "en" : "zh-CN");
}

function formatFocusDate(date) {
  return new Intl.DateTimeFormat(focusLocale, {weekday: "long", month: "long", day: "numeric"}).format(date);
}

function applyStaticTranslations() {
  document.documentElement.lang = focusLocale;
  document.querySelectorAll("[data-i18n]").forEach(element => {
    element.textContent = t(element.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-title]").forEach(element => {
    element.title = t(element.dataset.i18nTitle);
  });
  document.querySelectorAll("[data-i18n-aria-label]").forEach(element => {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel));
  });
  const button = document.querySelector("#languageButton");
  if (button) {
    button.textContent = focusLocale === "zh-CN" ? "EN" : "中";
    button.title = t(focusLocale === "zh-CN" ? "Switch to English" : "Switch to Simplified Chinese");
    button.setAttribute("aria-label", button.title);
  }
}

applyStaticTranslations();
